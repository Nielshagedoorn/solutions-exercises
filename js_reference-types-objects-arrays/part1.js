const person = {
  name: "Bert",
  age: 45,
  evaluations: [7, 10, 9],
};

// entire object
console.log(person);

// dot notation
console.log(person.name);
console.log(person.age);

// bracket notation
console.log(person["name"]);
console.log(person["age"]);

// extra key-value pair
console.log(person.evaluations);
